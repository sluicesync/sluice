// TestBuildFloatRepairBatchSQL_SinglePK pins the batched Postgres statement
// shape and the row-major, $N-numbered arg order for a single-column PK with
// two REAL SET columns. The FIRST row's placeholders carry the target-type
// casts (::BIGINT / ::REAL); subsequent rows are bare $N.
func TestBuildFloatRepairBatchSQL_SinglePK(t *testing.T) {
	colTypes := map[string]*ir.Column{
		"id": {Name: "id", Type: ir.Integer{Width: 64}},
		"a":  {Name: "a", Type: ir.Float{Precision: ir.FloatSingle}, Nullable: true},
		"b":  {Name: "b", Type: ir.Float{Precision: ir.FloatSingle}, Nullable: true},
	}
	batch := []ir.Row{
		{"id": int64(1), "a": float64(float32(8388608)), "b": nil},
		{"id": int64(2), "a": float64(float32(-123456.789)), "b": float64(float32(0.1))},
	}
	gotSQL, gotArgs, err := buildFloatRepairBatchSQL("public", "frepair", []string{"id"}, []string{"a", "b"}, batch, colTypes, emitOpts{})
	if err != nil {
		t.Fatal(err)
	}
	wantSQL := `UPDATE "public"."frepair" AS tgt SET "a" = v."a", "b" = v."b"` +
		` FROM (VALUES ($1::BIGINT, $2::REAL, $3::REAL), ($4, $5, $6))` +
		` AS v("id", "a", "b")` +
		` WHERE tgt."id" = v."id"`
	if gotSQL != wantSQL {
		t.Errorf("SQL mismatch:\n got: %s\nwant: %s", gotSQL, wantSQL)
	}

	wantArgs := []any{
		prep(t, batch[0]["id"], colTypes, "id"),
		prep(t, batch[0]["a"], colTypes, "a"),
		prep(t, batch[0]["b"], colTypes, "b"),
		prep(t, batch[1]["id"], colTypes, "id"),
		prep(t, batch[1]["a"], colTypes, "a"),
		prep(t, batch[1]["b"], colTypes, "b"),
	}
	if !reflect.DeepEqual(gotArgs, wantArgs) {
		t.Errorf("args mismatch:\n got: %#v\nwant: %#v", gotArgs, wantArgs)
	}
}

// TestBuildFloatRepairBatchSQL_NumericTarget pins the numeric-typed target
// family: a FLOAT source overridden to numeric(10,2) casts the first-row
// placeholder to ::NUMERIC(10,2), and the value passes through the SAME
// shaping the per-row path used (the numeric column's scale rounds on
// assignment, exactly as the per-row `SET c = $1` did).
func TestBuildFloatRepairBatchSQL_NumericTarget(t *testing.T) {
	colTypes := map[string]*ir.Column{
		"id": {Name: "id", Type: ir.Integer{Width: 32}},
		"n":  {Name: "n", Type: ir.Decimal{Precision: 10, Scale: 2}, Nullable: true},
	}
	batch := []ir.Row{{"id": int64(1), "n": float64(1.239)}}
	gotSQL, gotArgs, err := buildFloatRepairBatchSQL("public", "t", []string{"id"}, []string{"n"}, batch, colTypes, emitOpts{})
	if err != nil {
		t.Fatal(err)
	}
	wantSQL := `UPDATE "public"."t" AS tgt SET "n" = v."n"` +
		` FROM (VALUES ($1::INTEGER, $2::NUMERIC(10,2)))` +
		` AS v("id", "n")` +
		` WHERE tgt."id" = v."id"`
	if gotSQL != wantSQL {
		t.Errorf("SQL mismatch:\n got: %s\nwant: %s", gotSQL, wantSQL)
	}
	if !reflect.DeepEqual(gotArgs, []any{prep(t, int64(1), colTypes, "id"), prep(t, float64(1.239), colTypes, "n")}) {
		t.Errorf("args mismatch: %#v", gotArgs)
	}
}

// TestBuildFloatRepairBatchSQL_CompositePK pins the multi-column WHERE join.
func TestBuildFloatRepairBatchSQL_CompositePK(t *testing.T) {
	colTypes := map[string]*ir.Column{
		"x":  {Name: "x", Type: ir.Integer{Width: 32}},
		"y":  {Name: "y", Type: ir.Text{}},
		"fl": {Name: "fl", Type: ir.Float{Precision: ir.FloatDouble}, Nullable: true},
	}
	batch := []ir.Row{{"x": int64(1), "y": "k", "fl": float64(1.5)}}
	gotSQL, _, err := buildFloatRepairBatchSQL("public", "t", []string{"x", "y"}, []string{"fl"}, batch, colTypes, emitOpts{})
	if err != nil {
		t.Fatal(err)
	}
	wantSQL := `UPDATE "public"."t" AS tgt SET "fl" = v."fl"` +
		` FROM (VALUES ($1::INTEGER, $2::TEXT, $3::DOUBLE PRECISION))` +
		` AS v("x", "y", "fl")` +
		` WHERE tgt."x" = v."x" AND tgt."y" = v."y"`
	if gotSQL != wantSQL {
		t.Errorf("SQL mismatch:\n got: %s\nwant: %s", gotSQL, wantSQL)
	}
}

// TestFloatRepairCastType pins the cast-type dispatch across the column
// families a FLOAT-repair PK / SET column can take — the family the cast
// logic dispatches on. The load-bearing case is the AUTO_INCREMENT integer:
// emitColumnType would append " GENERATED BY DEFAULT AS IDENTITY" (an invalid
// CAST target), so integers must render via the storage-type helper.
func TestFloatRepairCastType(t *testing.T) {
	cases := []struct {
		name string
		typ  ir.Type
		want string
	}{
		{"bigint", ir.Integer{Width: 64}, "BIGINT"},
		{"integer", ir.Integer{Width: 32}, "INTEGER"},
		{"smallint", ir.Integer{Width: 16}, "SMALLINT"},
		{"auto_increment_pk", ir.Integer{Width: 64, AutoIncrement: true}, "BIGINT"},
		{"unsigned_widens", ir.Integer{Width: 32, Unsigned: true}, "BIGINT"},
		{"real", ir.Float{Precision: ir.FloatSingle}, "REAL"},
		{"double", ir.Float{Precision: ir.FloatDouble}, "DOUBLE PRECISION"},
		{"numeric", ir.Decimal{Precision: 10, Scale: 2}, "NUMERIC(10,2)"},
		{"numeric_unconstrained", ir.Decimal{Unconstrained: true}, "NUMERIC"},
		{"text", ir.Text{}, "TEXT"},
		{"varchar", ir.Varchar{Length: 20}, "VARCHAR(20)"},
		{"char", ir.Char{Length: 4}, "CHAR(4)"},
		{"uuid", ir.UUID{}, "UUID"},
		{"boolean", ir.Boolean{}, "BOOLEAN"},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			got, err := floatRepairCastType(&ir.Column{Name: "c", Type: tc.typ}, emitOpts{})
			if err != nil {
				t.Fatal(err)
			}
			if got != tc.want {
				t.Errorf("castType(%s) = %q, want %q", tc.name, got, tc.want)
			}
		})
	}

	// A nil column type (column absent from the target table) refuses loudly
	// rather than emitting a silently-wrong cast.
	if _, err := floatRepairCastType(nil, emitOpts{}); err == nil {
		t.Error("want error for nil column")
	}
}

func prep(t *testing.T, v any, colTypes map[string]*ir.Column, col string) any {
	t.Helper()
	out, err := prepareApplierValue(v, colTypes, col)
	if err != nil {
		t.Fatalf("prepareApplierValue(%v, %q): %v", v, col, err)
	}
	return out
}
