// End-to-end integration test for Phase 3.1 + 3.2: take a full
// backup, ALTER + INSERT on the source, take an incremental, restore
// the chain into a fresh target, and verify the data arrives.
//
// PG → PG covers:
//
//   - Acceptance criterion 1: incremental writer captures CDC events.
//   - Acceptance criterion 2: chain restore produces same data.
//   - Acceptance criterion 4: schema evolution within a chain.

package pipeline

