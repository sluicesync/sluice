// TestBackup_Encryption_PerChunkMode validates acceptance criterion
// for per-chunk mode: every chunk carries its own WrappedCEK; restore
// round-trips clean.
func TestBackup_Encryption_PerChunkMode(t *testing.T) {
	sourceDSN, targetDSN, cleanup := startPostgres(t)
	defer cleanup()

	applyDDL(t, sourceDSN, `
		CREATE TABLE users (
			id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			email VARCHAR(255) NOT NULL
		);
		INSERT INTO users (email) VALUES
			('alice@example.com'),
			('bob@example.com');
	`)

	pgEng, _ := engines.Get("postgres")
	dir := t.TempDir()
	store, err := NewLocalStore(dir)
	if err != nil {
		t.Fatalf("NewLocalStore: %v", err)
	}

	envBackup := newTestPassphraseEnvelope(t, "per-chunk-pass")
	if err := (&Backup{
		Source:    pgEng,
		SourceDSN: sourceDSN,
		Store:     store,
		ChunkRows: 1, // force multiple chunks
		Encryption: &BackupEncryption{
			Envelope: envBackup,
			Mode:     crypto.EncryptModePerChunk,
		},
	}).Run(context.Background()); err != nil {
		t.Fatalf("Backup.Run: %v", err)
	}

	root, err := readManifest(context.Background(), store)
	if err != nil {
		t.Fatalf("readManifest: %v", err)
	}
	if root.ChainEncryption == nil {
		t.Fatal("ChainEncryption nil")
	}
	if root.ChainEncryption.Mode != crypto.EncryptModePerChunk {
		t.Errorf("Mode: got %q", root.ChainEncryption.Mode)
	}
	// Per-chunk mode: chain-level WrappedCEK is empty.
	if len(root.ChainEncryption.WrappedCEK) != 0 {
		t.Errorf("per-chunk mode: ChainEncryption.WrappedCEK should be empty; got %d bytes", len(root.ChainEncryption.WrappedCEK))
	}
	// Each chunk's WrappedCEK is non-empty.
	if len(root.Tables) == 0 {
		t.Fatalf("no tables in manifest")
	}
	chunks := root.Tables[0].Chunks
	if len(chunks) < 2 {
		t.Fatalf("expected multiple chunks for per-chunk-mode test; got %d", len(chunks))
	}
	for i, c := range chunks {
		if c.Encryption == nil {
			t.Errorf("chunk %d: Encryption nil", i)
			continue
		}
		if len(c.Encryption.WrappedCEK) == 0 {
			t.Errorf("chunk %d: per-chunk WrappedCEK is empty", i)
		}
	}

	// Round-trip restore.
	envRestore := envelopeFromManifest(t, store, "per-chunk-pass")
	if err := (&Restore{
		Target:    pgEng,
		TargetDSN: targetDSN,
		Store:     store,
		Envelope:  envRestore,
	}).Run(context.Background()); err != nil {
		t.Fatalf("Restore.Run: %v", err)
	}

	got := pgQueryEmails(t, targetDSN)
	if len(got) != 2 {
		t.Errorf("got %d emails want 2: %v", len(got), got)
	}
}

// TestBackup_PlaintextBackwardCompat validates acceptance criterion 6:
// pre-Phase-6 (plaintext) chains continue to restore unchanged.
func TestBackup_PlaintextBackwardCompat(t *testing.T) {
	sourceDSN, targetDSN, cleanup := startPostgres(t)
	defer cleanup()

	applyDDL(t, sourceDSN, `
		CREATE TABLE users (
			id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			email VARCHAR(255) NOT NULL
		);
		INSERT INTO users (email) VALUES ('alice@example.com');
	`)

	pgEng, _ := engines.Get("postgres")
	dir := t.TempDir()
	store, err := NewLocalStore(dir)
	if err != nil {
		t.Fatalf("NewLocalStore: %v", err)
	}

	// Plaintext backup (no Encryption field).
	if err := (&Backup{
		Source:    pgEng,
		SourceDSN: sourceDSN,
		Store:     store,
	}).Run(context.Background()); err != nil {
		t.Fatalf("Backup.Run: %v", err)
	}

	root, err := readManifest(context.Background(), store)
	if err != nil {
		t.Fatalf("readManifest: %v", err)
	}
	if root.ChainEncryption != nil {
		t.Errorf("plaintext backup: ChainEncryption should be nil; got %+v", root.ChainEncryption)
	}

	// Restore without envelope; must succeed.
	if err := (&Restore{
		Target:    pgEng,
		TargetDSN: targetDSN,
		Store:     store,
	}).Run(context.Background()); err != nil {
		t.Fatalf("Restore.Run: %v", err)
	}

	got := pgQueryEmails(t, targetDSN)
	if len(got) != 1 || got[0] != "alice@example.com" {
		t.Errorf("got %v want [alice@example.com]", got)
	}
}

