// newTestPassphraseEnvelope returns a freshly-built passphrase
// envelope with default Argon2id params. Used by both the backup-side
// and restore-side construction in tests.
func newTestPassphraseEnvelope(t *testing.T, passphrase string) *crypto.PassphraseEnvelope {
	t.Helper()
	params, err := crypto.DefaultArgon2idParams()
	if err != nil {
		t.Fatalf("DefaultArgon2idParams: %v", err)
	}
	env, err := crypto.NewPassphraseEnvelope(passphrase, params)
	if err != nil {
		t.Fatalf("NewPassphraseEnvelope: %v", err)
	}
	return env
}

// envelopeFromManifest reads the chain root from store, extracts the
// recorded Argon2id params, and builds a restore-side
// PassphraseEnvelope using the supplied passphrase. Mirrors the CLI
// path's "read manifest, build envelope from recorded params" flow.
func envelopeFromManifest(t *testing.T, store ir.BackupStore, passphrase string) crypto.EnvelopeEncryption {
	t.Helper()
	root, err := ReadRootManifest(context.Background(), store)
	if err != nil {
		t.Fatalf("ReadRootManifest: %v", err)
	}
	if root == nil || root.ChainEncryption == nil || root.ChainEncryption.Argon2id == nil {
		t.Fatalf("manifest missing ChainEncryption.Argon2id")
	}
	p := root.ChainEncryption.Argon2id
	params := crypto.Argon2idParams{
		Salt:        p.Salt,
		Memory:      p.Memory,
		Iterations:  p.Iterations,
		Parallelism: p.Parallelism,
		KeyLen:      p.KeyLen,
	}
	env, err := crypto.NewPassphraseEnvelope(passphrase, params)
	if err != nil {
		t.Fatalf("NewPassphraseEnvelope: %v", err)
	}
	return env
}

// chunkContainsPlaintext reads a chunk file's bytes off disk and
// returns true if any of the supplied substrings appears as plaintext.
// Used by the round-trip test to confirm encryption actually happened.
func chunkContainsPlaintext(t *testing.T, store ir.BackupStore, chunkPath string, substrs ...string) bool {
	t.Helper()
	rc, err := store.Get(context.Background(), chunkPath)
	if err != nil {
		t.Fatalf("store.Get %q: %v", chunkPath, err)
	}
	defer func() { _ = rc.Close() }()
	b, err := io.ReadAll(rc)
	if err != nil {
		t.Fatalf("read chunk %q: %v", chunkPath, err)
	}
	for _, s := range substrs {
		if bytes.Contains(b, []byte(s)) {
			t.Logf("chunk %q contains plaintext substring %q (encryption broken?)", chunkPath, s)
			return true
		}
	}
	return false
}

// TestBackup_PassphraseEncryption_RoundTrip validates acceptance
// criterion 1: encrypt a PG full backup, verify chunks on disk are
// ciphertext, restore with the same passphrase, confirm data matches.
func TestBackup_PassphraseEncryption_RoundTrip(t *testing.T) {
	sourceDSN, targetDSN, cleanup := startPostgres(t)
	defer cleanup()

	const seedDDL = `
		CREATE TABLE users (
			id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			email VARCHAR(255) NOT NULL
		);
		INSERT INTO users (email) VALUES
			('alice@example.com'),
			('bob@example.com'),
			('carol@example.com');
	`
	applyDDL(t, sourceDSN, seedDDL)

	pgEng, ok := engines.Get("postgres")
	if !ok {
		t.Fatal("postgres engine not registered")
	}

	dir := t.TempDir()
	store, err := NewLocalStore(dir)
	if err != nil {
		t.Fatalf("NewLocalStore: %v", err)
	}

	const passphrase = "correct horse battery staple"
	envBackup := newTestPassphraseEnvelope(t, passphrase)

	if err := (&Backup{
		Source:        pgEng,
		SourceDSN:     sourceDSN,
		Store:         store,
		SluiceVersion: "v0.22.0-test",
		Encryption: &BackupEncryption{
			Envelope: envBackup,
			Mode:     crypto.EncryptModePerChain,
		},
	}).Run(context.Background()); err != nil {
		t.Fatalf("Backup.Run: %v", err)
	}

	// Manifest should carry encryption metadata.
	root, err := readManifest(context.Background(), store)
	if err != nil {
		t.Fatalf("readManifest: %v", err)
	}
	if root.ChainEncryption == nil {
		t.Fatal("ChainEncryption nil after encrypted backup")
	}
	if root.ChainEncryption.Algorithm != crypto.AlgorithmAESGCM {
		t.Errorf("Algorithm: got %q", root.ChainEncryption.Algorithm)
	}
	if root.ChainEncryption.Mode != crypto.EncryptModePerChain {
		t.Errorf("Mode: got %q", root.ChainEncryption.Mode)
	}
	if root.ChainEncryption.KEKMode != crypto.KEKModePassphrase {
		t.Errorf("KEKMode: got %q", root.ChainEncryption.KEKMode)
	}
	if len(root.ChainEncryption.WrappedCEK) == 0 {
		t.Errorf("WrappedCEK empty")
	}
	if root.ChainEncryption.Argon2id == nil {
		t.Errorf("Argon2id params missing")
	}

	// Each chunk's manifest entry should carry encryption metadata too.
	if len(root.Tables) == 0 || len(root.Tables[0].Chunks) == 0 {
		t.Fatalf("no chunks in manifest")
	}
	chunk := root.Tables[0].Chunks[0]
	if chunk.Encryption == nil {
		t.Errorf("ChunkInfo.Encryption nil")
	}
	if len(chunk.Encryption.WrappedCEK) != 0 {
		t.Errorf("per-chain mode: ChunkInfo.Encryption.WrappedCEK should be empty; got %d bytes", len(chunk.Encryption.WrappedCEK))
	}

	// On-disk chunk bytes must NOT contain any of the plaintext values.
	chunkPath := chunk.File
	if chunkContainsPlaintext(t, store, chunkPath, "alice", "bob", "carol", "@example.com") {
		t.Errorf("encrypted chunk on disk contains plaintext rows; encryption layer broken")
	}

	// Restore with the matching passphrase.
	envRestore := envelopeFromManifest(t, store, passphrase)
	if err := (&Restore{
		Target:    pgEng,
		TargetDSN: targetDSN,
		Store:     store,
		Envelope:  envRestore,
	}).Run(context.Background()); err != nil {
		t.Fatalf("Restore.Run: %v", err)
	}

	got := pgQueryEmails(t, targetDSN)
	gotMap := map[string]bool{}
	for _, e := range got {
		gotMap[e] = true
	}
	for _, want := range []string{"alice@example.com", "bob@example.com", "carol@example.com"} {
		if !gotMap[want] {
			t.Errorf("target missing email %q (got %v)", want, got)
		}
	}
}

// TestBackup_Encryption_WrongPassphrase validates acceptance
