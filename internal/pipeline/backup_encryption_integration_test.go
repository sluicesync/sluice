// TestBackup_Encryption_LocalStoreDir confirms the chunk file on disk
// passes a plaintext sniff test in a freshly-located filesystem path
// (unrelated to the round-trip test, kept distinct so a future bug
// in LocalStore's path mapping doesn't escape).
func TestBackup_Encryption_LocalStoreDir(t *testing.T) {
	sourceDSN, _, cleanup := startPostgres(t)
	defer cleanup()

	applyDDL(t, sourceDSN, `
		CREATE TABLE users (id BIGINT PRIMARY KEY, email VARCHAR(255));
		INSERT INTO users VALUES (1, 'forensic-marker@example.com');
	`)

	pgEng, _ := engines.Get("postgres")
	dir := t.TempDir()
	store, err := NewLocalStore(dir)
	if err != nil {
		t.Fatalf("NewLocalStore: %v", err)
	}

	env := newTestPassphraseEnvelope(t, "forensic-pass")
	if err := (&Backup{
		Source:    pgEng,
		SourceDSN: sourceDSN,
		Store:     store,
		Encryption: &BackupEncryption{
			Envelope: env,
			Mode:     crypto.EncryptModePerChain,
		},
	}).Run(context.Background()); err != nil {
		t.Fatalf("Backup.Run: %v", err)
	}

	root, err := readManifest(context.Background(), store)
	if err != nil {
		t.Fatalf("readManifest: %v", err)
	}
	for _, table := range root.Tables {
		for _, c := range table.Chunks {
			abs := filepath.Join(dir, filepath.FromSlash(c.File))
			data, rerr := readFileForTest(abs)
			if rerr != nil {
				t.Fatalf("read chunk %q: %v", abs, rerr)
			}
			if bytes.Contains(data, []byte("forensic-marker")) {
				t.Errorf("chunk %q on disk contains plaintext marker; encryption broken", abs)
			}
			if bytes.Contains(data, []byte("@example.com")) {
				t.Errorf("chunk %q on disk contains plaintext domain", abs)
			}
		}
	}
}

// readFileForTest is a small wrapper over os.ReadFile that surfaces a
// clear error for the integration tests.
func readFileForTest(path string) ([]byte, error) {
	return os.ReadFile(path)
}
