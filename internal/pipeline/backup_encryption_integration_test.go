// Phase 6.1 integration tests: passphrase-mode envelope encryption.
// Covers the load-bearing acceptance criteria from
// `docs/dev/design-logical-backups-phase-6.md`:
//
//   1. Round-trip — encrypted backup + correct-passphrase restore lands
//      the original data byte-for-byte; chunk bytes on disk are NOT
//      plaintext.
//   2. Wrong-passphrase refusal — restore with the wrong key fails
//      with auth-tag-mismatch error; no partial data lands.
//   3. Missing-key refusal — encrypted chain restored without an
//      envelope refuses at chain-walk time with operator-actionable
//      error.
//   4. Per-chunk mode — each chunk's manifest entry carries its own
//      WrappedCEK (verified by inspection); restore round-trips clean.
//   5. Plaintext backward-compat — pre-Phase-6 chains restore
//      unchanged.

package pipeline

