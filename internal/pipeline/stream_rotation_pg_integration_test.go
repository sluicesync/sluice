// ADR-0046 load-bearing integration matrix (Postgres, testcontainers):
//
//   - Zero-loss multi-segment rotation under continuous write load:
//     drive a KNOWN insert sequence spanning several inline rotations,
//     restore the multi-segment lineage, assert the EXACT final state
//     (every row exactly once — no loss, no duplication across any
//     segment boundary).
//   - Crash-injection matrix: kill at EVERY FSM edge (post-drain,
//     post-snapshot, post-bulkcopy, pre-commit-write,
//     post-commit-write) -> restart -> assert no data loss + correct
//     <=COMMIT / >COMMIT resolution + restore correctness.
//   - Never-rotated one-segment lineage restores byte-identical data
//     to a pre-ADR single chain (strict-generalization proof).
//   - Mixed-codec (none + gzip + zstd) lineage restores correctly.
//   - Prune-on-lineage (drop a leading whole segment) keeps
//     restore-after-prune correct.

