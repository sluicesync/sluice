// Shape A end-to-end integration test — closes the v0.72.0 gap that
// allowed Bug 80 (reader-projection regression) and Bug 81 (preflight
// no-op) to ship. The Shape A unit-tests pinned individual pieces;
// nothing exercised the full migrate path with a populated target.
// "Validate end-to-end before building more" — this test is the
// floor that should have caught both bugs pre-release.

