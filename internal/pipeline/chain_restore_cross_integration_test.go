// mysqlQueryEmailsActive returns email → active for every row in the
// users table on the MySQL target. active is decoded as bool from
// TINYINT(1).
func mysqlQueryEmailsActive(t *testing.T, dsn string) map[string]bool {
	t.Helper()
	db, err := sql.Open("mysql", dsn)
	if err != nil {
		t.Fatalf("open: %v", err)
	}
	defer func() { _ = db.Close() }()
	ctx, cancel := context.WithTimeout(context.Background(), 10*time.Second)
	defer cancel()
	rows, err := db.QueryContext(ctx, "SELECT email, active FROM users ORDER BY id")
	if err != nil {
		t.Fatalf("query: %v", err)
	}
	defer func() { _ = rows.Close() }()
	out := map[string]bool{}
	for rows.Next() {
		var email string
		var active int64 // TINYINT(1) decodes as int64
		if err := rows.Scan(&email, &active); err != nil {
			t.Fatalf("scan: %v", err)
		}
		out[email] = active != 0
	}
	if err := rows.Err(); err != nil {
		t.Fatalf("rows: %v", err)
	}
	return out
}

