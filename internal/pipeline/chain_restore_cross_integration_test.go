// Phase 5 integration tests: cross-engine chain restore.
//
// PG-rooted chain → MySQL target and the reverse direction.
// Acceptance criteria 1-4 from design-logical-backups-phase-5.md:
//
//   1. PG-rooted chain → MySQL target restore (full + 2 incrementals).
//   2. MySQL-rooted chain → PG target restore (mix of INSERT/UPDATE/DELETE).
//   3. Cross-engine schema evolution mid-stream (broker-driven).
//   4. Loud refusal for unsupportable types (PostGIS).
//
// Each test boots the minimum container set needed and bounds itself to
// 60s. Acceptance criterion 6 (same-engine paths regression-clean) is
// covered by the existing same-engine chain-restore integration tests
// running unchanged.

