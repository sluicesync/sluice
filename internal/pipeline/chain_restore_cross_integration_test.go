// mysqlQueryTagByEmail reads the tag column for the row with the
// supplied email, or "" if no such row or no tag column yet (the
// schema-evolution test polls until the column appears). Tolerant of
// missing-column SQL errors so the broker's catch-up window works.
func mysqlQueryTagByEmail(t *testing.T, dsn, email string) string {
	t.Helper()
	db, err := sql.Open("mysql", dsn)
	if err != nil {
		return ""
	}
	defer func() { _ = db.Close() }()
	ctx, cancel := context.WithTimeout(context.Background(), 5*time.Second)
	defer cancel()
	var tag sql.NullString
	row := db.QueryRowContext(ctx, "SELECT tag FROM customers WHERE email = ?", email)
	if err := row.Scan(&tag); err != nil {
		// Missing column or row returns "".
		if strings.Contains(err.Error(), "Unknown column") || strings.Contains(err.Error(), "no rows") {
			return ""
		}
		return ""
	}
	if !tag.Valid {
		return ""
	}
	return tag.String
}
