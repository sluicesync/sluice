// ADR-0154 Phase 3a — AWS KMS manifest SIGNING against a localstack KMS
// container (asymmetric ECDSA key). Runs under `integration && kmsverify`
// alongside the KMS-ENCRYPTION legs (backup_kms_localstack_integration_
// test.go), reusing its localstack + Postgres helpers.
//
// What's covered (live, against localstack KMS + real PG):
//
//   1. KMS-signed + KMS-encrypted full + incremental chain → chain restore
//      byte-exact, with signatures verified via the exported PUBLIC key
//      (the offline DR path: verification holds only the public key, never
//      the KMS Sign grant).
//   2. Store-tamper leg — corrupting a signed manifest after the fact makes
//      the restore REFUSE with SLUICE-E-BACKUP-SIGNATURE-INVALID (the
//      signature caught exactly the substitution it exists to catch).
//   3. Keyless verification — the restore host is given only the public key
//      (fetched once via GetPublicKey), proving KMS Sign access is not
//      needed to verify.

