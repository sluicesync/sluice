// Standalone-sequence round-trip pins (item-51 TRIAGE finding #1).
//
// Value-level ground truth first: the silent-loss class was "the
// target's nextval() produces DIFFERENT numbers than the source
// would", so the load-bearing assertions here are actual INSERTs /
// nextval() calls on the migrated target — catalog-text equality is
// the dump-parity oracle's job (migrate_dump_parity_integration_test.go).
//
// The options matrix is the Bug-74 discipline applied to sequences:
// the reader/writer dispatch on the sequence's option/ownership
// SHAPE (ascending vs descending, type family, cycle/cache, owned vs
// unowned vs shared, serial-collapsible vs re-optioned), so each
// shape family gets its own pinned sequence rather than one
// representative.

package pipeline

