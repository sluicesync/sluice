// TestBackupStream_Postgres_SignalDrainExitsClean drives the SIGTERM-
// shape path: cancel ctx in the middle of an active stream, verify
// Run returns nil within stopDrainTimeout (clean drain).
//
// Acceptance criterion 6.
func TestBackupStream_Postgres_SignalDrainExitsClean(t *testing.T) {
	sourceDSN, _, cleanup := startPostgresLogical(t)
	defer cleanup()

	const seedDDL = `
		CREATE TABLE users (
			id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			email VARCHAR(255) NOT NULL
		);
		ALTER TABLE users REPLICA IDENTITY FULL;
		INSERT INTO users (email) VALUES ('alice@example.com');
	`
	applyDDL(t, sourceDSN, seedDDL)

	pgEng, _ := engines.Get("postgres")
	dir := t.TempDir()
	store, _ := NewLocalStore(dir)

	applyDDL(t, sourceDSN, `CREATE PUBLICATION sluice_pub FOR ALL TABLES`)
	slotLSN, err := createPGLogicalSlotReturningLSN(t, sourceDSN, "sluice_slot")
	if err != nil {
		t.Fatalf("create slot: %v", err)
	}
	defer dropPGLogicalSlot(t, sourceDSN, "sluice_slot")

	if err := (&Backup{
		Source: pgEng, SourceDSN: sourceDSN, Store: store, SluiceVersion: "test",
	}).Run(context.Background()); err != nil {
		t.Fatalf("Backup.Run: %v", err)
	}
	full, _ := readManifest(context.Background(), store)
	full.Kind = ir.BackupKindFull
	full.EndPosition = ir.Position{
		Engine: "postgres",
		Token:  fmt.Sprintf(`{"slot":"sluice_slot","lsn":%q}`, slotLSN),
	}
	full.BackupID = ir.ComputeBackupID(full)
	_ = writeManifestAt(context.Background(), store, ManifestFileName, full)

	// Drive concurrent inserts so the stream is mid-rollover when ctx
	// fires.
	var wg sync.WaitGroup
	insertCtx, insertCancel := context.WithCancel(context.Background())
	defer insertCancel()
	wg.Add(1)
	go func() {
		defer wg.Done()
		i := 0
		for {
			select {
			case <-insertCtx.Done():
				return
			default:
			}
			applyDDL(t, sourceDSN, fmt.Sprintf(
				`INSERT INTO users (email) VALUES ('streaming%d@example.com');`, i))
			i++
			time.Sleep(50 * time.Millisecond)
		}
	}()

	stream := &BackupStream{
		Source:             pgEng,
		SourceDSN:          sourceDSN,
		Store:              store,
		ParentRef:          full.BackupID,
		RolloverWindow:     5 * time.Second,
		RolloverMaxChanges: 1_000_000,
		RolloverMaxBytes:   1 << 30,
		SluiceVersion:      "test",
	}

	ctx, cancel := context.WithCancel(context.Background())
	streamErr := make(chan error, 1)
	go func() { streamErr <- stream.Run(ctx) }()

	// Let the stream observe some inserts, then cancel.
	time.Sleep(2 * time.Second)
	cancel()

	insertCancel()
	wg.Wait()

	select {
	case err := <-streamErr:
		if err != nil {
			t.Errorf("stream.Run after ctx.Cancel = %v; want nil (clean exit)", err)
		}
	case <-time.After(15 * time.Second):
		t.Fatal("stream.Run did not exit within 15s of ctx.Cancel")
	}
}
