// Integration tests for Phase 4 — long-running backup stream — against
// a real Postgres testcontainer. Mirrors the shape of the Phase 3.1
// incremental tests in incremental_pg_integration_test.go.
//
// Acceptance criteria covered (per design-logical-backups-phase-4.md):
//
//   1. PG long-running stream produces rolling incrementals.
//   3. Time-bound rollover triggers (quiet source + small window).
//   4. Size-bound rollover triggers (drive N inserts → expected count).
//   5. Restore from stream-written chain works (chain-walker
//      transparently handles stream-written chains).
//   6. SIGTERM clean exit (modeled via ctx.Cancel mid-stream).
//   7. backup stream stop cross-machine (write stop_requested_at from
//      a separate goroutine; stream observes and exits).
//   8. Concurrent-writer protection refuses second stream.

