// TestBackupStream_Postgres_ConcurrentWriterRefused exercises the
// concurrent-writer protection: start one stream, attempt a second
// without --force, verify the second refuses.
//
// Acceptance criterion 8.
func TestBackupStream_Postgres_ConcurrentWriterRefused(t *testing.T) {
	sourceDSN, _, cleanup := startPostgresLogical(t)
	defer cleanup()

	const seedDDL = `
		CREATE TABLE users (
			id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			email VARCHAR(255) NOT NULL
		);
		ALTER TABLE users REPLICA IDENTITY FULL;
		INSERT INTO users (email) VALUES ('alice@example.com');
	`
	applyDDL(t, sourceDSN, seedDDL)

	pgEng, _ := engines.Get("postgres")
	dir := t.TempDir()
	store, _ := NewLocalStore(dir)

	applyDDL(t, sourceDSN, `CREATE PUBLICATION sluice_pub FOR ALL TABLES`)
	slotLSN, err := createPGLogicalSlotReturningLSN(t, sourceDSN, "sluice_slot")
	if err != nil {
		t.Fatalf("create slot: %v", err)
	}
	defer dropPGLogicalSlot(t, sourceDSN, "sluice_slot")

	if err := (&Backup{
		Source: pgEng, SourceDSN: sourceDSN, Store: store, SluiceVersion: "test",
	}).Run(context.Background()); err != nil {
		t.Fatalf("Backup.Run: %v", err)
	}
	full, _ := readManifest(context.Background(), store)
	full.Kind = ir.BackupKindFull
	full.EndPosition = ir.Position{
		Engine: "postgres",
		Token:  fmt.Sprintf(`{"slot":"sluice_slot","lsn":%q}`, slotLSN),
	}
	full.BackupID = ir.ComputeBackupID(full)
	_ = writeManifestAt(context.Background(), store, ManifestFileName, full)

	// Pre-seed a fresh stream_state.json mimicking another running
	// stream on a different host. This is what a real second-process
	// would race against.
	now := time.Now().UTC()
	preexisting := &streamState{
		PID: 99999, Host: "other.example.com",
		StartedAt:      now.Add(-10 * time.Second),
		LastRolloverAt: now.Add(-1 * time.Second), // very fresh
	}
	if err := writeStreamState(context.Background(), store, DefaultStreamStateFilename, preexisting); err != nil {
		t.Fatalf("seed stream_state: %v", err)
	}

	stream := &BackupStream{
		Source:             pgEng,
		SourceDSN:          sourceDSN,
		Store:              store,
		ParentRef:          full.BackupID,
		RolloverWindow:     time.Minute, // recent threshold = 2 min
		RolloverMaxChanges: 100,
		SluiceVersion:      "test",
	}

	err = stream.Run(context.Background())
	if err == nil {
		t.Fatal("err = nil; want concurrent-writer refusal")
	}
	if !strings.Contains(err.Error(), "stream is already running") {
		t.Errorf("err = %v; want concurrent-writer guidance", err)
	}

	// With --force, the second stream should accept the takeover.
	stream.Force = true
	ctx, cancel := context.WithCancel(context.Background())
	streamErr := make(chan error, 1)
	go func() { streamErr <- stream.Run(ctx) }()

	// Let it tick at least once.
	time.Sleep(3 * time.Second)
	cancel()

	select {
	case err := <-streamErr:
		if err != nil {
			t.Errorf("stream.Run with --force after cancel = %v; want nil", err)
		}
	case <-time.After(10 * time.Second):
		t.Fatal("stream.Run did not exit within 10s")
	}

	// Verify the state file now reflects our pid (not the pre-seeded
	// 99999), which proves we took over.
	got, err := readStreamState(context.Background(), store, DefaultStreamStateFilename)
	if err != nil {
		t.Fatalf("readStreamState: %v", err)
	}
	if got.PID == 99999 {
		t.Errorf("state file pid = 99999; expected our process's pid (takeover did not write fresh state)")
	}
}

