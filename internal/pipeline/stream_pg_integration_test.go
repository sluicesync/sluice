// TestBackupStream_Postgres_RolloverByMaxChanges is the load-bearing
// happy-path test: drive ~25 inserts under a stream with
// --rollover-max-changes=10, observe 3 rollovers, restore the chain
// into a fresh target, verify every row arrives.
//
// Acceptance criteria 1, 4, 5.
func TestBackupStream_Postgres_RolloverByMaxChanges(t *testing.T) {
	sourceDSN, targetDSN, cleanup := startPostgresLogical(t)
	defer cleanup()

	const seedDDL = `
		CREATE TABLE users (
			id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			email VARCHAR(255) NOT NULL
		);
		ALTER TABLE users REPLICA IDENTITY FULL;
		INSERT INTO users (email) VALUES ('alice@example.com'), ('bob@example.com');
	`
	applyDDL(t, sourceDSN, seedDDL)

	pgEng, ok := engines.Get("postgres")
	if !ok {
		t.Fatal("postgres engine not registered")
	}

	dir := t.TempDir()
	store, err := NewLocalStore(dir)
	if err != nil {
		t.Fatalf("NewLocalStore: %v", err)
	}

	applyDDL(t, sourceDSN, `CREATE PUBLICATION sluice_pub FOR ALL TABLES`)
	slotLSN, err := createPGLogicalSlotReturningLSN(t, sourceDSN, "sluice_slot")
	if err != nil {
		t.Fatalf("create slot: %v", err)
	}
	defer dropPGLogicalSlot(t, sourceDSN, "sluice_slot")

	// Full backup (parent for the stream chain).
	if err := (&Backup{
		Source: pgEng, SourceDSN: sourceDSN, Store: store, SluiceVersion: "test",
	}).Run(context.Background()); err != nil {
		t.Fatalf("Backup.Run: %v", err)
	}
	full, _ := readManifest(context.Background(), store)
	full.Kind = ir.BackupKindFull
	full.EndPosition = ir.Position{
		Engine: "postgres",
		Token:  fmt.Sprintf(`{"slot":"sluice_slot","lsn":%q}`, slotLSN),
	}
	full.BackupID = ir.ComputeBackupID(full)
	if err := writeManifestAt(context.Background(), store, ManifestFileName, full); err != nil {
		t.Fatalf("rewrite full: %v", err)
	}

	// Drive 25 inserts on the source AFTER the slot is created, so
	// they're retained in WAL and visible to the stream.
	for i := 0; i < 25; i++ {
		applyDDL(t, sourceDSN, fmt.Sprintf(
			`INSERT INTO users (email) VALUES ('user%d@example.com');`, i))
	}

	// Run the stream with small bounds so it commits multiple
	// rollovers quickly. The CDC pump's channel will close once we
	// cancel ctx after observing enough rollovers.
	ctx, cancel := context.WithTimeout(context.Background(), 60*time.Second)
	defer cancel()

	stream := &BackupStream{
		Source:             pgEng,
		SourceDSN:          sourceDSN,
		Store:              store,
		ParentRef:          full.BackupID,
		RolloverWindow:     2 * time.Second,
		RolloverMaxChanges: 10,
		RolloverMaxBytes:   1 << 30,
		ChunkChanges:       100,
		SluiceVersion:      "test",
	}

	streamErr := make(chan error, 1)
	go func() { streamErr <- stream.Run(ctx) }()

	// Watch for rollovers; cancel once we have at least 2.
	deadline := time.Now().Add(45 * time.Second)
	for time.Now().Before(deadline) {
		records, _ := listAllManifests(context.Background(), store)
		var incrCount int
		for _, r := range records {
			if r.manifest.Kind == ir.BackupKindIncremental {
				incrCount++
			}
		}
		if incrCount >= 2 {
			break
		}
		time.Sleep(500 * time.Millisecond)
	}
	cancel()

	select {
	case err := <-streamErr:
		if err != nil {
			t.Errorf("stream.Run after cancel = %v; want nil (clean exit)", err)
		}
	case <-time.After(10 * time.Second):
		t.Fatal("stream.Run did not exit within 10s of ctx.Cancel")
	}

	// Inspect manifests; expect at least 2 incrementals.
	records, _ := listAllManifests(context.Background(), store)
	var incrementals []*ir.Manifest
	for _, r := range records {
		if r.manifest.Kind == ir.BackupKindIncremental {
			incrementals = append(incrementals, r.manifest)
		}
	}
	if len(incrementals) < 2 {
		t.Fatalf("incremental rollovers = %d; want >= 2", len(incrementals))
	}

	// Verify chain (every chunk's hash matches what's on disk).
	total, mismatches, err := VerifyBackup(context.Background(), store)
	if err != nil {
		t.Fatalf("VerifyBackup: %v", err)
	}
	if mismatches != 0 {
		t.Errorf("VerifyBackup: %d of %d chunks failed", mismatches, total)
	}

	// Chain restore into the fresh target.
	if err := (&Restore{
		Target: pgEng, TargetDSN: targetDSN, Store: store,
	}).Run(context.Background()); err != nil {
		t.Fatalf("Restore.Run: %v", err)
	}

	// Verify every email is on the target.
	got := pgQueryEmails(t, targetDSN)
	want := map[string]bool{
		"alice@example.com": true,
		"bob@example.com":   true,
	}
	for i := 0; i < 25; i++ {
		want[fmt.Sprintf("user%d@example.com", i)] = true
	}
	gotMap := map[string]bool{}
	for _, e := range got {
		gotMap[e] = true
	}
	for w := range want {
		if !gotMap[w] {
			t.Errorf("target missing email %q (got count: %d)", w, len(got))
		}
	}
}

