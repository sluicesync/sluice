// TestMigrate_PG_PgTrgm_NotEnabled_DropsOpclass pins the loud-failure
// shape when --enable-pg-extension pg_trgm is NOT set. The schema
// reader's per-opclass gate (extensionOperatorClassEnabled) only
// fires when the operator opted into the extension; without that
// opt-in, the opclass is dropped from the IR and the writer emits
// the index without it. PG then rejects the CREATE INDEX with
// "operator class \"<x>\" does not exist for access method \"<y>\""
// — which on the index-creation phase surfaces as a Migrator.Run
// error, preserving the loud-failure tenet.
func TestMigrate_PG_PgTrgm_NotEnabled_DropsOpclass(t *testing.T) {
	sourceDSN, targetDSN, cleanup := startPostgresWithTrgm(t, true)
	defer cleanup()

	const seedDDL = `
		CREATE TABLE documents (
			id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			body TEXT NOT NULL
		);

		CREATE INDEX documents_body_trgm
		    ON documents USING GIN (body gin_trgm_ops);
	`
	applyPGDDL(t, sourceDSN, seedDDL)

	pgEng, _ := engines.Get("postgres")
	mig := &Migrator{
		Source:    pgEng,
		Target:    pgEng,
		SourceDSN: sourceDSN,
		TargetDSN: targetDSN,
		// Deliberately NO EnabledPGExtensions — the opt-in tenet
		// means a pg_trgm-using source without the flag must fail
		// loudly rather than silently dropping the opclass and
		// emitting a useless index.
	}

	ctx, cancel := context.WithTimeout(context.Background(), 60*time.Second)
	defer cancel()
	err := mig.Run(ctx)
	if err == nil {
		t.Fatal("Migrator.Run = nil; want failure when opclass dropped from IR")
	}
	// PG's error: `data type text has no default operator class for
	// access method "gin"`. Be lenient on exact wording — assert on
	// the load-bearing fragments.
	msg := strings.ToLower(err.Error())
	if !strings.Contains(msg, "operator class") && !strings.Contains(msg, "default") {
		t.Errorf("err = %v; want mention of \"operator class\" / \"default\"", err)
	}
}

