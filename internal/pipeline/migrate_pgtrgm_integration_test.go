// PG → PG pg_trgm passthrough integration tests (ADR-0032 Tier 2 lite).
// pg_trgm is "operator-class only" — no new column types, just
// `gin_trgm_ops` / `gist_trgm_ops` operator classes on existing
// `text` / `varchar` columns. The test boots a stock postgres:16
// container (pg_trgm ships in the standard contrib bundle), runs
// CREATE EXTENSION on both source and target, exercises the migrate
// path with a `CREATE INDEX ... USING GIN (col gin_trgm_ops)` index,
// and verifies the round-trip preserves the index's operator class
// on the target.

