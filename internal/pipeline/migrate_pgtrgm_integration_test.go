// TestMigrate_PG_PgTrgm_CrossEngineToMySQL_RefusedAtPreflight pins
// the v0.30.1 wire-up: simple-mode migrate now runs
// checkCrossEngineSupportable before any data moves, so a PG source
// with a pg_trgm-indexed table targeting MySQL fails at preflight
// with an actionable refusal — NOT silently bulk-copies and then
// dies cryptically at MySQL Error 1170 during the indexes phase.
// Mirrors the existing chain_restore preflight; the gap was that
// migrate.go never called the helper.
func TestMigrate_PG_PgTrgm_CrossEngineToMySQL_RefusedAtPreflight(t *testing.T) {
	sourceDSN, _, pgCleanup := startPostgresWithTrgm(t, true)
	defer pgCleanup()

	_, mysqlTargetDSN, mysqlCleanup := startMySQL(t)
	defer mysqlCleanup()

	const seedDDL = `
		CREATE TABLE documents (
			id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			body TEXT NOT NULL
		);
		INSERT INTO documents (body) VALUES ('the quick brown fox');
		CREATE INDEX documents_body_trgm
		    ON documents USING GIN (body gin_trgm_ops);
	`
	applyPGDDL(t, sourceDSN, seedDDL)

	pgEng, _ := engines.Get("postgres")
	mysqlEng, _ := engines.Get("mysql")
	mig := &Migrator{
		Source:    pgEng,
		Target:    mysqlEng,
		SourceDSN: sourceDSN,
		TargetDSN: mysqlTargetDSN,
		// Deliberately NO EnabledPGExtensions — that flag pairing with
		// a non-PG target would short-circuit at the source-side
		// applyEnabledPGExtensions gate, which is a separate and
		// earlier refusal mechanism. The scenario this test exercises
		// is the operator running a vanilla cross-engine migrate on a
		// PG source that *happens* to have a pg_trgm-indexed table —
		// the v0.30.0 cycle showed that without the v0.30.1 migrate-
		// path preflight wire-up + the AM-based check in
		// unsupportablePGIndexToMySQL, the operator gets MySQL Error
		// 1170 after bulk-copy. Pinning that refusal here.
	}

	ctx, cancel := context.WithTimeout(context.Background(), 60*time.Second)
	defer cancel()
	err := mig.Run(ctx)
	if err == nil {
		t.Fatal("Migrator.Run = nil; want cross-engine preflight refusal")
	}
	// Two distinct refusal mechanisms can fire here:
	//   1. applyEnabledPGExtensions refuses when --enable-pg-extension
	//      is paired with a non-PG target (source-side reader gate; the
	//      earliest gate in the migrate pipeline).
	//   2. The v0.30.1 migrate-path preflight via
	//      unsupportablePGIndexToMySQL refuses on the opclass-bearing
	//      IndexColumn (runs after ApplyMappings; only fires when (1)
	//      didn't already catch it).
	// Either is correct — both happen before any data moves. Assert on
	// the load-bearing operator-visible property: error message names
	// MySQL or cross-engine, and the target's documents table doesn't
	// exist.
	msg := err.Error()
	if !strings.Contains(msg, "mysql") && !strings.Contains(msg, "MySQL") &&
		!strings.Contains(msg, "cross-engine") {
		t.Errorf("err = %v; want mention of MySQL target or cross-engine refusal", err)
	}

	// Load-bearing: the refusal must fire BEFORE any rows reach MySQL.
	// Open the MySQL target and confirm the documents table doesn't
	// exist (preflight bailed before CreateTables).
	mysqlDB, openErr := sql.Open("mysql", mysqlTargetDSN)
	if openErr != nil {
		t.Fatalf("open mysql target: %v", openErr)
	}
	defer func() { _ = mysqlDB.Close() }()
	var name string
	err = mysqlDB.QueryRowContext(ctx,
		`SELECT table_name FROM information_schema.tables
		 WHERE  table_schema = DATABASE() AND table_name = 'documents'`).Scan(&name)
	if err == nil {
		t.Errorf("documents table exists on MySQL target after preflight refusal — wanted empty target")
	} else if !errors.Is(err, sql.ErrNoRows) {
		// driver couldn't run the query at all; that's also fine —
		// what matters is the table isn't there.
		t.Logf("table-existence query did not return ErrNoRows: %v (acceptable)", err)
	}
}

// TestMigrate_PG_Bug65_ExpressionIndexWithOpclass is the ADR-0045 §3
// (Bug 65, D3 full-carry) ground truth: the PG schema reader's
// populateIndexes used to NOT query pg_index.indexprs, so an
// expression-only index entry (indkey `0` sentinel, empty attname)
// hit `if colName == "" { continue }` and was SILENTLY DROPPED — the
// whole functional index vanished from the IR. ADR-0045 surfaces it
// via pg_get_indexdef into IndexColumn.Expression /
// ExpressionDialect="postgres", the PG-source analogue of the
// MySQL-source post-Bug-16 path.
//
// The index here is BOTH an expression index AND opclass-bearing
// (`USING GIN (lower(body) gin_trgm_ops)`), which is the Bug 47 /
// ADR-0032 interaction the ADR flags: surfacing the expression must
// not regress the independent opclass capture. Asserted on a PG→PG
// round-trip (the reader change is what's under test) plus an IR
// read-back proving Expression + OperatorClass both populate.
func TestMigrate_PG_Bug65_ExpressionIndexWithOpclass(t *testing.T) {
	sourceDSN, targetDSN, cleanup := startPostgresWithTrgm(t, true)
	defer cleanup()

	const seedDDL = `
		CREATE TABLE documents (
			id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			body TEXT NOT NULL
		);

		INSERT INTO documents (body) VALUES
			('The Quick Brown Fox'),
			('JUMPS over the Lazy Dog');

		-- Expression index, no opclass: pre-ADR-0045 this was dropped
		-- silently (Bug 65). lower(body) is a functional key.
		CREATE INDEX documents_lower_body ON documents (lower(body));

		-- Expression index WITH an extension opclass: Bug 65 must
		-- surface the expression AND not regress Bug 47 opclass capture.
		CREATE INDEX documents_lower_body_trgm
		    ON documents USING GIN (lower(body) gin_trgm_ops);
	`
	applyPGDDL(t, sourceDSN, seedDDL)

	pgEng, ok := engines.Get("postgres")
	if !ok {
		t.Fatal("postgres engine not registered")
	}
	mig := &Migrator{
		Source:              pgEng,
		Target:              pgEng,
		SourceDSN:           sourceDSN,
		TargetDSN:           targetDSN,
		EnabledPGExtensions: []string{"pg_trgm"},
	}
	ctx, cancel := context.WithTimeout(context.Background(), 90*time.Second)
	defer cancel()
	if err := mig.Run(ctx); err != nil {
		t.Fatalf("Migrator.Run: %v", err)
	}

	tgtDB, err := sql.Open("pgx", targetDSN)
	if err != nil {
		t.Fatalf("open target: %v", err)
	}
	defer func() { _ = tgtDB.Close() }()

	// Both expression indexes must exist on the target — pre-ADR-0045
	// they'd be absent (silently dropped at read).
	for _, idxName := range []string{"documents_lower_body", "documents_lower_body_trgm"} {
		var n int
		if err := tgtDB.QueryRowContext(ctx, `
			SELECT COUNT(*)
			FROM   pg_index ix
			JOIN   pg_class i  ON i.oid = ix.indexrelid
			JOIN   pg_class cl ON cl.oid = ix.indrelid
			WHERE  cl.relname = 'documents' AND i.relname = $1`, idxName).Scan(&n); err != nil {
			t.Fatalf("index existence query (%s): %v", idxName, err)
		}
		if n == 0 {
			t.Errorf("expression index %q missing on target — Bug 65 regression (silent drop)", idxName)
		}
	}

	// The opclass on the expression index must be preserved (Bug 47
	// non-regression on an expression-bearing index).
	var amname, opcname string
	if err := tgtDB.QueryRowContext(ctx, `
		SELECT am.amname, opc.opcname
		FROM   pg_index ix
		JOIN   pg_class i  ON i.oid = ix.indexrelid
		JOIN   pg_class cl ON cl.oid = ix.indrelid
		JOIN   pg_am    am ON am.oid = i.relam
		JOIN   LATERAL unnest(ix.indclass) WITH ORDINALITY AS uc(opcoid, ord) ON TRUE
		JOIN   pg_opclass opc ON opc.oid = uc.opcoid
		WHERE  cl.relname = 'documents' AND i.relname = 'documents_lower_body_trgm'`).
		Scan(&amname, &opcname); err != nil {
		t.Fatalf("opclass query: %v", err)
	}
	if amname != "gin" {
		t.Errorf("expression index AM = %q; want gin", amname)
	}
	if opcname != "gin_trgm_ops" {
		t.Errorf("expression index opclass = %q; want gin_trgm_ops (Bug 47 regressed by Bug 65 fix)", opcname)
	}

	// IR read-back: the rebuilt schema must carry Expression +
	// ExpressionDialect on the expression index entries, and the
	// opclass on the trgm one — proving the reader populates the IR
	// (not just that PG happened to recreate it).
	pgRdr, err := pgEng.OpenSchemaReader(ctx, targetDSN)
	if err != nil {
		t.Fatalf("OpenSchemaReader on target: %v", err)
	}
	defer closeIf(pgRdr)
	if aware, ok := pgRdr.(ir.ExtensionAware); ok {
		if err := aware.EnableExtensions(ctx, []string{"pg_trgm"}); err != nil {
			t.Fatalf("EnableExtensions on target reader: %v", err)
		}
	}
	got, err := pgRdr.ReadSchema(ctx)
	if err != nil {
		t.Fatalf("ReadSchema on target: %v", err)
	}
	docs := findTable(got, "documents")
	if docs == nil {
		t.Fatal("target has no documents table")
	}
	var sawPlainExpr, sawTrgmExpr bool
	for _, idx := range docs.Indexes {
		for _, c := range idx.Columns {
			if c.Expression == "" {
				continue
			}
			if c.ExpressionDialect != "postgres" {
				t.Errorf("index %q expr dialect = %q; want postgres", idx.Name, c.ExpressionDialect)
			}
			switch idx.Name {
			case "documents_lower_body":
				sawPlainExpr = true
			case "documents_lower_body_trgm":
				sawTrgmExpr = true
				if c.OperatorClass != "gin_trgm_ops" {
					t.Errorf("trgm expression index opclass in IR = %q; want gin_trgm_ops",
						c.OperatorClass)
				}
			}
		}
	}
	if !sawPlainExpr {
		t.Errorf("plain expression index not surfaced into IR; indexes=%+v", docs.Indexes)
	}
	if !sawTrgmExpr {
		t.Errorf("opclass-bearing expression index not surfaced into IR; indexes=%+v", docs.Indexes)
	}
}
