// Task #42: cross-engine case-sensitivity test matrix — PG ↔ MySQL.
//
// Bulk-copy (Migrator) and CDC (Streamer) in both directions:
//   - PG → MySQL bulk + CDC
//   - MySQL → PG bulk + CDC
//
// Cross-engine is where the case-preservation invariant earns its
// keep. A source-side reader that lower-cases an identifier corrupts
// every downstream consumer regardless of target. A writer that drops
// quotes can write to a folded name on the target while the IR carries
// the correct case. The matrix exercises both source readers and both
// target writers in every cell, so any regression on either side
// surfaces immediately.
//
// Same Phase A scope discipline: quoted source DDL only.
// `lower_case_table_names=0` is forced on every MySQL container in
// this file (source AND target as needed) via
// startMySQLCaseSensitive / startMySQLBinlogCaseSensitive (defined in
// migrate_case_preservation_mysql_integration_test.go in this same
// package).

package pipeline

import (
	"context"
	"fmt"
	"testing"
	"time"

	"sluicesync.dev/sluice/internal/engines"

	_ "sluicesync.dev/sluice/internal/engines/mysql"
	_ "sluicesync.dev/sluice/internal/engines/postgres"
)

// TestMigrate_CasePreservation_PostgresToMySQL pins cross-engine case
// preservation in the PG → MySQL direction on the bulk-copy path.
func TestMigrate_CasePreservation_PostgresToMySQL(t *testing.T) {
	for _, shape := range caseShapes {
		shape := shape
		t.Run(shape.label, func(t *testing.T) {
			pgSource, _, pgCleanup := startPostgres(t)
			defer pgCleanup()

			_, mysqlTarget, mysqlCleanup := startMySQLCaseSensitive(t)
			defer mysqlCleanup()

			seedDDL := fmt.Sprintf(`
				CREATE TABLE %q (
					id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
					%q VARCHAR(255) NOT NULL
				);
				INSERT INTO %q (%q) VALUES ('alice'), ('bob');
			`, shape.table, shape.column, shape.table, shape.column)
			applyPGDDL(t, pgSource, seedDDL)

			pgEng, ok := engines.Get("postgres")
			if !ok {
				t.Fatal("postgres engine not registered")
			}
			mysqlEng, ok := engines.Get("mysql")
			if !ok {
				t.Fatal("mysql engine not registered")
			}

			mig := &Migrator{
				Source:    pgEng,
				Target:    mysqlEng,
				SourceDSN: pgSource,
				TargetDSN: mysqlTarget,
			}
			ctx, cancel := context.WithTimeout(context.Background(), 2*time.Minute)
			defer cancel()
			if err := mig.Run(ctx); err != nil {
				t.Fatalf("Migrator.Run (PG→MySQL): %v", err)
			}

			assertMySQLTableCasePreserved(t, ctx, mysqlTarget, shape)
			assertMySQLRowsPreserved(t, ctx, mysqlTarget, shape, 2)
		})
	}
}

