// Phase 3.3 integration tests for `sluice sync start
// --position-from-manifest` on PG sources. Covers the design doc's
// Acceptance Criteria 6, 7, 8 plus the EndPosition-recording
// criterion 1:
//
//   - Criterion 1: a v0.17.2 full backup records EndPosition; the
//     "parent has no EndPosition" warning no longer fires.
//   - Criterion 6: chain handoff with the slot still in place — the
//     CDC reader resumes via slot reuse.
//   - Criterion 7: chain handoff with the slot dropped but
//     wal_keep_size sufficient — soft warnings emitted; no refusal.
//   - Criterion 8: chain handoff with the slot dropped AND
//     wal_keep_size insufficient — refusal naming the gap.
//   - Criterion 10 (mocked-equivalent): Patroni-detection signals on
//     a real PG cluster aren't testable without a Patroni front-end,
//     so we cover the detection logic with the role-name signal.

