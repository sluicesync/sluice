// TestBackup_RecordsEndPosition_PostgresIntegration pins acceptance
// criterion 1: a v0.17.2 PG full backup records a non-empty
// EndPosition, with the slot/lsn shape the chain incremental can
// resume from.
func TestBackup_RecordsEndPosition_PostgresIntegration(t *testing.T) {
	sourceDSN, _, cleanup := startPostgresLogical(t)
	defer cleanup()

	const seedDDL = `
		CREATE TABLE users (
			id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			email VARCHAR(255) NOT NULL
		);
		ALTER TABLE users REPLICA IDENTITY FULL;
		INSERT INTO users (email) VALUES ('alice@example.com');
	`
	applyDDL(t, sourceDSN, seedDDL)

	pgEng, ok := engines.Get("postgres")
	if !ok {
		t.Fatal("postgres engine not registered")
	}

	dir := t.TempDir()
	store, err := blobcodec.NewLocalStore(dir)
	if err != nil {
		t.Fatalf("NewLocalStore: %v", err)
	}
	if err := (&Backup{
		Source: pgEng, SourceDSN: sourceDSN, Store: store,
		SluiceVersion: "v0.17.2-test",
		SlotName:      "sluice_slot",
	}).Run(context.Background()); err != nil {
		t.Fatalf("Backup.Run: %v", err)
	}
	full, err := readManifest(context.Background(), store)
	if err != nil {
		t.Fatalf("readManifest: %v", err)
	}
	if full.EndPosition.Engine != "postgres" {
		t.Errorf("EndPosition.Engine = %q; want postgres", full.EndPosition.Engine)
	}
	if !strings.Contains(full.EndPosition.Token, `"slot":"sluice_slot"`) {
		t.Errorf("EndPosition.Token = %q; want containing slot=sluice_slot", full.EndPosition.Token)
	}
	if !strings.Contains(full.EndPosition.Token, `"lsn":`) {
		t.Errorf("EndPosition.Token = %q; want containing lsn=", full.EndPosition.Token)
	}
	if full.BackupID == "" {
		t.Error("BackupID empty after EndPosition recording")
	}
	if full.Kind != irbackup.BackupKindFull {
		t.Errorf("Kind = %q; want %q", full.Kind, irbackup.BackupKindFull)
	}
}

// TestStreamer_SyncStart_PositionFromManifest_PG_SlotExists pins acceptance
// criterion 6: a chain restored into a target plus
// `sync start --position-from-manifest` reuses the existing slot to
// resume CDC. The full's recorded EndPosition becomes the resume
// position; the slot is created fresh before CDC starts so its
// restart_lsn covers the chain terminal.
func TestStreamer_SyncStart_PositionFromManifest_PG_SlotExists(t *testing.T) {
	sourceDSN, targetDSN, cleanup := startPostgresLogical(t)
	defer cleanup()

	const seedDDL = `
		CREATE TABLE users (
			id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			email VARCHAR(255) NOT NULL
		);
		ALTER TABLE users REPLICA IDENTITY FULL;
		INSERT INTO users (email) VALUES ('alice@example.com');
	`
	applyDDL(t, sourceDSN, seedDDL)

	pgEng, _ := engines.Get("postgres")
	dir := t.TempDir()
	store, _ := blobcodec.NewLocalStore(dir)

	// Create publication + slot BEFORE the backup so the slot's
	// restart_lsn covers the EndPosition recorded by the full.
	applyDDL(t, sourceDSN, `CREATE PUBLICATION sluice_pub FOR ALL TABLES`)
	if _, err := createPGLogicalSlotReturningLSN(t, sourceDSN, "sluice_slot"); err != nil {
		t.Fatalf("create slot: %v", err)
	}
	defer dropPGLogicalSlot(t, sourceDSN, "sluice_slot")

	// Take the full — Phase 3.3.A records EndPosition automatically.
	if err := (&Backup{
		Source: pgEng, SourceDSN: sourceDSN, Store: store,
		SluiceVersion: "v0.17.2-test",
		SlotName:      "sluice_slot",
	}).Run(context.Background()); err != nil {
		t.Fatalf("Backup.Run: %v", err)
	}

	// Restore into target (the chain has no incrementals; restore is
	// just the full). After restore, target has alice but the
	// sluice_cdc_state row is absent — that's the surface
	// --position-from-manifest is designed for.
	if err := (&Restore{
		Target: pgEng, TargetDSN: targetDSN, Store: store,
	}).Run(context.Background()); err != nil {
		t.Fatalf("Restore.Run: %v", err)
	}

	// Insert ON SOURCE between backup and sync — these changes must
	// be picked up by the chain handoff, NOT lost.
	applyDDL(t, sourceDSN, `INSERT INTO users (email) VALUES ('bob@example.com'), ('carol@example.com')`)

	// Now run a brief sync with --position-from-manifest.
	ctx, cancel := context.WithTimeout(context.Background(), 30*time.Second)
	defer cancel()
	streamer := &Streamer{
		Source:                    pgEng,
		Target:                    pgEng,
		SourceDSN:                 sourceDSN,
		TargetDSN:                 targetDSN,
		StreamID:                  "test-position-from-manifest-pg",
		PositionFromManifestStore: store,
	}
	runErr := make(chan error, 1)
	go func() { runErr <- streamer.Run(ctx) }()
	if !waitForRowCount(t, targetDSN, "users", 3, 20*time.Second) {
		cancel()
		<-runErr
		t.Fatal("target did not reach 3 rows; CDC handoff didn't catch up")
	}
	cancel()
	if err := <-runErr; err != nil && !isContextCanceled(err) {
		t.Fatalf("streamer.Run: %v", err)
	}

	emails := pgQueryEmails(t, targetDSN)
	want := map[string]bool{"alice@example.com": true, "bob@example.com": true, "carol@example.com": true}
	got := map[string]bool{}
	for _, e := range emails {
		got[e] = true
	}
	for w := range want {
		if !got[w] {
			t.Errorf("missing email %q on target; got %v", w, emails)
		}
	}
}

// TestStreamer_SyncStart_PositionFromManifest_PG_SlotMissing_Refuses pins
// acceptance criterion 6's failure mode and 8's headline: when the
// slot named in the chain's terminal position doesn't exist on the
// source, Phase 3.3.C's preflight refuses with a clear error rather
// than silently proceeding to a slot-creation that would advance
// past the chain's terminal LSN and miss data.
func TestStreamer_SyncStart_PositionFromManifest_PG_SlotMissing_Refuses(t *testing.T) {
	sourceDSN, targetDSN, cleanup := startPostgresLogical(t)
	defer cleanup()

	applyDDL(t, sourceDSN, `
		CREATE TABLE users (
			id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			email VARCHAR(255) NOT NULL
		);
		ALTER TABLE users REPLICA IDENTITY FULL;
		INSERT INTO users (email) VALUES ('alice@example.com');
	`)
	pgEng, _ := engines.Get("postgres")
	dir := t.TempDir()
	store, _ := blobcodec.NewLocalStore(dir)

	// Create slot, take backup, then DROP slot before sync — simulates
	// the "the slot was abandoned long enough that something cleaned
	// it up" case the design doc covers.
	applyDDL(t, sourceDSN, `CREATE PUBLICATION sluice_pub FOR ALL TABLES`)
	if _, err := createPGLogicalSlotReturningLSN(t, sourceDSN, "sluice_slot"); err != nil {
		t.Fatalf("create slot: %v", err)
	}

	if err := (&Backup{
		Source: pgEng, SourceDSN: sourceDSN, Store: store,
		SluiceVersion: "v0.17.2-test", SlotName: "sluice_slot",
	}).Run(context.Background()); err != nil {
		t.Fatalf("Backup.Run: %v", err)
	}
	if err := (&Restore{Target: pgEng, TargetDSN: targetDSN, Store: store}).Run(context.Background()); err != nil {
		t.Fatalf("Restore.Run: %v", err)
	}

	// Drop the slot so the preflight's check 3 trips.
	dropPGLogicalSlot(t, sourceDSN, "sluice_slot")

	ctx, cancel := context.WithTimeout(context.Background(), 30*time.Second)
	defer cancel()
	streamer := &Streamer{
		Source:                    pgEng,
		Target:                    pgEng,
		SourceDSN:                 sourceDSN,
		TargetDSN:                 targetDSN,
		StreamID:                  "test-slot-missing",
		PositionFromManifestStore: store,
		SlotName:                  "sluice_slot",
	}
	err := streamer.Run(ctx)
	if err == nil {
		t.Fatal("Streamer.Run: nil; want preflight refusal on missing slot")
	}
	if !strings.Contains(err.Error(), "slot") || !strings.Contains(err.Error(), "does not exist") {
		t.Errorf("err = %v; want refusal naming the missing slot", err)
	}
}

