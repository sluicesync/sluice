// TestStreamer_Preflight_PG_DetectsLowWalKeepSize confirms the preflight
// query side: a low wal_keep_size on the source produces a warning
// (without StrictPreflight, the warning logs but the run proceeds).
// We exercise the preflight directly via the SchemaReader surface so
// the test doesn't have to wait for a CDC stream.
func TestStreamer_Preflight_PG_DetectsLowWalKeepSize(t *testing.T) {
	sourceDSN, _, cleanup := startPostgresLogical(t)
	defer cleanup()

	// Set wal_keep_size below the v0.17.2 warn threshold.
	db, err := sql.Open("pgx", sourceDSN)
	if err != nil {
		t.Fatalf("open: %v", err)
	}
	defer func() { _ = db.Close() }()
	if _, err := db.ExecContext(context.Background(), `ALTER SYSTEM SET wal_keep_size = '16MB'`); err != nil {
		t.Fatalf("set wal_keep_size: %v", err)
	}
	if _, err := db.ExecContext(context.Background(), `SELECT pg_reload_conf()`); err != nil {
		t.Fatalf("reload: %v", err)
	}

	pgEng, _ := engines.Get("postgres")
	sr, err := pgEng.OpenSchemaReader(context.Background(), sourceDSN)
	if err != nil {
		t.Fatalf("OpenSchemaReader: %v", err)
	}
	defer closeIf(sr)

	preflighter, ok := sr.(PositionFromManifestPreflight)
	if !ok {
		t.Fatal("PG SchemaReader does not implement PositionFromManifestPreflight")
	}
	chainPos := ir.Position{Engine: "postgres", Token: `{"slot":"sluice_slot","lsn":"0/100"}`}
	report, err := preflighter.PreflightPositionFromManifest(context.Background(), chainPos, "sluice_slot")
	if err != nil {
		t.Fatalf("PreflightPositionFromManifest: %v", err)
	}
	// Slot doesn't exist on a fresh PG; expect a refusal naming the
	// missing slot, plus a wal_keep warning.
	if report.Refusal == "" {
		t.Errorf("expected refusal for missing slot; got empty Refusal, warnings=%v", report.Warnings)
	}
	if !strings.Contains(report.Refusal, "does not exist") {
		t.Errorf("refusal = %q; want 'does not exist'", report.Refusal)
	}
	var sawWalKeep bool
	for _, w := range report.Warnings {
		if strings.Contains(w, "wal_keep_size") {
			sawWalKeep = true
		}
	}
	if !sawWalKeep {
		t.Errorf("expected wal_keep_size warning; got %v", report.Warnings)
	}
}

// TestStreamer_Preflight_PG_DetectsPhysicalSlot confirms Bug 36 (v0.17.3)
// Signal 4: a non-temporary physical replication slot on the source
// is detected as an HA-cluster signal. Most non-HA PG deployments
// don't carry standby physical slots, so the presence of one is a
// strong heuristic — and it covers managed-PG services where the
// v0.17.2 SQL signals (Patroni-prefixed GUCs / pg_stat_replication /
// patroni-named roles) all systematically miss.
func TestStreamer_Preflight_PG_DetectsPhysicalSlot(t *testing.T) {
	sourceDSN, _, cleanup := startPostgresLogical(t)
	defer cleanup()

	db, err := sql.Open("pgx", sourceDSN)
	if err != nil {
		t.Fatalf("open: %v", err)
	}
	defer func() { _ = db.Close() }()

	// Create a non-temporary physical replication slot to simulate
	// the HA-cluster shape.
	if _, err := db.ExecContext(
		context.Background(),
		`SELECT pg_create_physical_replication_slot('sluice_test_phys_slot', false, false)`,
	); err != nil {
		t.Fatalf("create physical slot: %v", err)
	}
	defer func() {
		_, _ = db.ExecContext(context.Background(),
			`SELECT pg_drop_replication_slot('sluice_test_phys_slot')`)
	}()

	pgEng, _ := engines.Get("postgres")
	sr, err := pgEng.OpenSchemaReader(context.Background(), sourceDSN)
	if err != nil {
		t.Fatalf("OpenSchemaReader: %v", err)
	}
	defer closeIf(sr)

	preflighter, ok := sr.(PositionFromManifestPreflight)
	if !ok {
		t.Fatal("PG SchemaReader does not implement PositionFromManifestPreflight")
	}
	chainPos := ir.Position{Engine: "postgres", Token: `{"slot":"sluice_slot","lsn":"0/100"}`}
	report, err := preflighter.PreflightPositionFromManifest(context.Background(), chainPos, "sluice_slot")
	if err != nil {
		t.Fatalf("PreflightPositionFromManifest: %v", err)
	}
	var sawPatroni bool
	for _, w := range report.Warnings {
		if strings.HasPrefix(w, "this PG cluster is HA-managed") {
			sawPatroni = true
			if !strings.Contains(w, "physical replication slots") {
				t.Errorf("Patroni warning = %q; want 'physical replication slots' substring", w)
			}
		}
	}
	if !sawPatroni {
		t.Errorf("expected Patroni warning citing physical slot; got %v", report.Warnings)
	}
}

